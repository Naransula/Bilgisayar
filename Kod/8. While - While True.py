# Döngüden önce sayaç tanımlanarak döngü kurulduğunda sayaç ifadesiyle
# döngünün ne kadar çalışacağı belirtilir. DÖngü içinde sayaç belirtilen
# kurala göre artırılır yada azaltılır. Döngü içinde sayaç artırılmaz
# veya azaltılmazsa döngü sonsuz döngüye girer.

sayac=0

sayac=sayac+1 #1'er artarsa
sayac=sayac+2 #2'şer artarsa
sayac=sayac-1 #1'er azalırsa

